//******************************************************************************
//   MSP432P401 Demo - Write a word to Port A (Port 1 + 2)
//
//   Description: Drive all 16 pins of Port A as output, configure output levels
//   of the 16 pins with a single 16-bit write.
//
//                MSP432P401x
//             -----------------
//         /|\|                 |
//          | |                 |
//          --|RST              |
//            |                 |
//            |                 |
//
//   Dung Dang
//   Texas Instruments Inc.
//   Oct 2016 (updated) | November 2013 (created)
//   Built with CCSv6.1, IAR, Keil, GCC
//******************************************************************************
#include "ti/devices/msp432p4xx/inc/msp.h"

int main(void)
{
    WDT_A->CTL = WDT_A_CTL_PW |             // Stop WDT
            WDT_A_CTL_HOLD;

    PA->DIR = 0xFFFF;                       // Port A (1+2) configured as output

    PA->OUT = 0x1234;                       // Configure output levels of all 16 pins

    // Go to LPM3

    /* Setting the sleep deep bit */
    SCB->SCR |= (SCB_SCR_SLEEPDEEP_Msk);

    // Ensures SLEEPDEEP takes effect immediately
    __DSB();

    __sleep();
    __no_operation();                       // For debugger
}
